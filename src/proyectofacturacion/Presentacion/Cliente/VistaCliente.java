    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtCodigo.getText().isEmpty() || txtNombre.getText().isEmpty() || txtTel.getText().isEmpty() || txtApellido.getText().isEmpty()
                || txtDNI.getText().isEmpty() || txtDireccion.getText().isEmpty() || txtRUC.getText().isEmpty()
                || cbxSexo.getModel().getSelectedItem().toString().equals("") || txtCorreo.getText().isEmpty()
                || txtTel.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Señor usuario Ingrese los datos del producto!", "INGRESAR", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                String sql = "insert into tblcliente(codigo,nombre,apellido,genero,dni,telefono,"
                        + "ruc,email,direcion,codmunicipio) values(?,?,?,?,?,?,?,?,?,?)";
                PreparedStatement stm = con.conexion().prepareStatement(sql);
                stm.setString(1, txtCodigo.getText());
                stm.setString(2, txtNombre.getText());
                stm.setString(3, txtApellido.getText());
                stm.setString(4, cbxSexo.getModel().getSelectedItem().toString());
                stm.setString(5, txtDNI.getText());
                stm.setString(6, txtTel.getText());
                stm.setString(7, txtRUC.getText());
                stm.setString(8, txtCorreo.getText());
                stm.setString(9, txtDireccion.getText());
                stm.setInt(10, cbxMunicipio.getSelectedIndex());
                int r = stm.executeUpdate();
                limpiar();
                mostrarDatos();

                if (r > 0) {
                    JOptionPane.showMessageDialog(null, "Cliente guardado correctamente");
                }

            } catch (SQLException ex) {
                Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

