    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int fila = tblClientes.getSelectedRow();

        if (fila >= 0) {
            txtCodigo.setText(tblClientes.getValueAt(fila, 0).toString());
            txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
            txtApellido.setText(tblClientes.getValueAt(fila, 2).toString());
            cbxSexo.setSelectedItem(tblClientes.getValueAt(fila, 3).toString());
            txtDNI.setText(tblClientes.getValueAt(fila, 4).toString());
            txtRUC.setText(tblClientes.getValueAt(fila, 5).toString());
            txtTel.setText(tblClientes.getValueAt(fila, 6).toString());
            txtCorreo.setText(tblClientes.getValueAt(fila, 7).toString());
            txtDireccion.setText(tblClientes.getValueAt(fila, 8).toString());          
            cbxMunicipio.setSelectedItem(tblClientes.getValueAt(fila, 9).toString());

        } else {
            JOptionPane.showMessageDialog(null, "No se selecciono la fila", "Atencion", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        try {
            PreparedStatement stm = con.conexion().prepareStatement("update tblcliente set codigo='"
                    + txtCodigo.getText() + "', nombre='" + txtNombre.getText() + "', apellido='"
                    + txtApellido.getText() + "', genero='" + cbxSexo.getModel().getSelectedItem().toString()
                    + "', dni='" + txtDNI.getText() + "', telefono='" + txtTel.getText() 
                    + "', ruc='" + txtRUC.getText()
                    + "', email='" + txtCorreo.getText() + "', direcion='" + txtDireccion.getText()
                    + "', codmunicipio='" + cbxMunicipio.getSelectedIndex()
                    + "' where codigo='" + txtCodigo.getText() + "'");
            stm.executeUpdate();
            mostrarDatos();
            JOptionPane.showMessageDialog(null, "Producto Actualzido!");

        } catch (SQLException ex) {
            Logger.getLogger(FormProducto.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnModificarActionPerformed

