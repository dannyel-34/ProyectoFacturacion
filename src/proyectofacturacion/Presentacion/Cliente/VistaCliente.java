    public void mostrarDatos() {

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("CODIGO");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("GENERO");
        modelo.addColumn("DNI");
        modelo.addColumn("RUC");
        modelo.addColumn("TELEFONO");
        modelo.addColumn("EMAIL");
        modelo.addColumn("DIRECCION");
        modelo.addColumn("PROVINCIA");
        tblClientes.setModel(modelo);

        String[] datos = new String[10];

        try {
            Statement st = con.conexion().createStatement();
            ResultSet rs = st.executeQuery("select * from tblcliente");

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);
                datos[9] = rs.getString(10);
                modelo.addRow(datos);
            }
            tblClientes.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void mostrarDatosMunicipio() {
        String sql = "select nombre from municipio";
        try {
            Statement st = con.conexion().createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                this.cbxMunicipio.addItem(rs.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(VistaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

